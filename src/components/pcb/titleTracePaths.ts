/**
 * Только Manhattan: сегменты строго H/V, углы 90° (как дорожки / «змейка» на сетке).
 */

export type Polyline = [number, number][];

const GAP = 0.35;

const WIDTH: Record<string, number> = {
  M: 6,
  W: 7,
  I: 3,
  T: 5,
  "1": 4,
  ".": 2,
  "-": 4,
};

function gw(ch: string): number {
  return WIDTH[ch] ?? 5;
}

export function manhattanRoute(
  from: [number, number],
  to: [number, number],
): Polyline {
  const [fx, fy] = from;
  const [tx, ty] = to;
  if (fx === tx && fy === ty) return [from];
  if (fx === tx || fy === ty) return [from, to];
  return [from, [tx, fy], to];
}

/** Разводка от точки к буквам — только ортогональ (без «диагонального fanout»). */
export function fanoutLines(
  polylines: Polyline[],
  origin: [number, number],
): Polyline[] {
  const targets = polylines.map((pl) => pl[0]).filter(Boolean);
  const extras = targets.map((t) => manhattanRoute(origin, t));
  return [...extras, ...polylines];
}

export type GlyphGroup = {
  char: string;
  polylines: Polyline[];
};

/** Полилинии по одной букве/символу (для анимации «буква за буквой»). */
export function polylinesGroupedByChar(s: string): GlyphGroup[] {
  const upper = s.toUpperCase().replace(/\s/g, "");
  let x = 0;
  const groups: GlyphGroup[] = [];
  for (const ch of upper) {
    const g = GLYPHS[ch];
    const w = gw(ch);
    if (!g) {
      x += w * 0.55 + GAP;
      continue;
    }
    const polylines = g.map((ln) => ln.map(([px, py]) => [px + x, py] as [number, number]));
    groups.push({ char: ch, polylines });
    x += w + GAP;
  }
  return groups;
}

export function polylinesForString(s: string): Polyline[] {
  return polylinesGroupedByChar(s).flatMap((g) => g.polylines);
}

